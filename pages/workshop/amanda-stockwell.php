                        <div class="about-workshop__description">
                            <p class="about-workshop__title">Amanda Stockwell</p>
                            <p class="about-workshop__subtitle">Getting Started with Lean Experimentation</p>
                            <div class="about-workshop__text">
                                <p>
                                    Heard of Lean Startup but not sure how it applies in your organization? The approach has become increasingly popular, but it can be hard to know where to get started, especially if you’re not in a true startup. In reality, the Lean Startup approach is a way to apply the scientific method across business and product development to reduce risk, which can be helpful to any kind of organization.
                                </p>
                                <p class="subtitle">
                                    In this session, I’ll provide:
                                </p>
                                <ul>
                                    <li>An overview of the Lean Startup approach</li>
                                    <li>Description of the Build Measure Learn framework</li>
                                    <li>Discussion of different types of MVPs</li>
                                    <li>Steps for successful implementation of lean experiments, including</li>
                                    <li>Start with an observed problem</li>
                                    <li> Write an effective hypothesis</li>
                                    <li>Craft your experiment</li>
                                    <li>Collaboratively generate MVP</li>
                                    <li>Analyze data and incorporate</li>
                                </ul>
                                <p class="subtitle">
                                    Detailed Schedule
                                </p>
                                <ul>
                                    <li>Introduction (15 minutes)</li>
                                    <li>I’ll introduce myself, my background, and depending on number of participants, ask each to introduce themselves as well.</li>
                                </ul>
                                <p class="subtitle">
                                    Lean Startup Overview (30 minutes)
                                </p>
                                <ul>
                                    <li>I’ll begin by giving some context the background of Lean Startup, where the approach originated, and how it’s being used across software companies of all sizes. I’ll do an overview of the key concepts, including:</li>
                                    <li>Entrepreneurship is everywhere</li>
                                    <li>Waste elimination</li>
                                    <li>Innovation accounting and validated learning</li>
                                    <li>The build measure learn cycle</li>
                                </ul>
                                <p class="subtitle">
                                    Running Lean Experiments (2.5 hours total)
                                </p>
                                <ul>
                                    <li>Once we’ve discussed the basic concepts, we’ll shift into the practical elements of successfully running lean experiments. I’ll provide an outline of a process and we’ll dive into each of the following areas:</li>
                                    <li>Starting with an observed problem and assumptions list</li>
                                    <li>Write an effective hypothesis</li>
                                    <li>Craft your experiment and choosing your MVP</li>
                                    <li>Analyze experimental data</li>
                                    <li>Incorporating results, reducing cycle time, and maintaining high level picture</li>
                                </ul>
                                <p class="subtitle">
                                    Problem Observation and Assumption Building (25 minutes)
                                </p>
                                <p>
                                    I’ll discuss the Lean Startup concept of GOOB (getting out of the building), how closely that mirrors traditional UX discovery practices of interviewing and observation, and the context for crafting problem and assumption statements, with examples of good and not so good statements.
                                </p>
                                <p class="subtitle">
                                    Hypothesis Setting (35 minutes)
                                </p>
                                <p>
                                    Next we’ll talk about the importance of setting clear hypotheses and I’ll provide my framework for doing so:
                                    <br/>If we [do, build, provide x thing], then [these people] will [some desirable outcome]. We’ll know when [actionable metrics].
                                </p>
                                <p>
                                    I’ll talk through how to set each element, with a special focus on setting the actionable metrics, as this is often the trickiest thing for new folks. I’ll talk through perceived costs, perceived value, failure conditions, and the focus on choosing something that will really help you make a business decision.
                                </p>
                                <p class="subtitle">
                                    Experiment and MVP Design (55 minutes)
                                </p>
                                <p>
                                    Next, we’ll discuss how to use your hypothesis to guide what sort of experiment you need to run, which goes hand in hand with choosing and building your MVP.
                                </p>
                                <p class="subtitle">
                                    I’ll walk through the specific uses, variations, and pros and cons of the following types of MVP:
                                </p>
                                <ul>
                                    <li>Traditional UX tactics applied as MVPs, such as surveys or data mining for generating ideas about customers or prototypes to test features)</li>
                                    <li>Wizard of Oz (looks real but isn’t - Zappos is famous example)</li>
                                    <li>Concierge (manual service that looks automated - no tech involved - Food on the Table example)</li>
                                    <li>Piecemeal (essentially a blend of Wizard of Oz/Concierge but you use existing tools)</li>
                                    <li>Shadow button/coming soon (you appear to add a feature/function but don’t build it out and monitor how many attempted uses you get)</li>
                                    <li>Landing Page (gauge interest by creating marketing materials and measuring response)</li>
                                    <li>Explainer video (Dropbox is famous example - demonstrate functionality without having to build)</li>
                                    <li>Crowdfunding (Sell before you build with things like Kickstarter or IndieGoGo)</li>
                                </ul>
                                <p class="subtitle">
                                    Conclusion and questions (15 minutes)
                                </p>
                                <p>
                                    Although I intend to answer questions throughout, I’ll take the final few moments to wrap up and answer outstanding questions.
                                </p>

                                <p class="subtitle">
                                    Target audience
                                </p>
                                <p>
                                    This session is for anyone interested in incorporating the kind of fast experimentation cycles that are core to the Lean Startup methodology. The session could be appropriate for designers, developers, product folks, scrum masters, or really anyone with a vested interest in quickly vetting ideas and incorporating feedback.
                                </p>
                                <p class="subtitle">
                                    No specific previous experience is required regarding Lean or running experiments, though I’ll be referencing UX research practices. */
